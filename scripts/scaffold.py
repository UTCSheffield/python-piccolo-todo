"""
scaffold_htmx.py
================
Generates a starter server-rendered frontend from your Piccolo table definitions.

Usage (run from the project root):
    python scripts/scaffold_htmx.py                  # plain Jinja2 (default)
    python scripts/scaffold_htmx.py --style htmx     # HTMX partial-swap version

Styles
------
  plain (default)
    Pure HTML forms + Post/Redirect/Get.  No JavaScript required.
    Every form posts to the server; the server redirects back to the page.
    Best for teaching HTML/Python before introducing client-side concepts.

  htmx
    Same routes but mutations return HTML fragments that HTMX swaps in-place.
    Requires htmx.min.js (served from /static/).
    Feels more "modern app" without a full JavaScript framework.

Both styles share the same Piccolo introspection logic and produce the same
editable starting-point files.  Re-running will NOT overwrite files unless you
pass --force.

Options:
  --style         plain (default) or htmx
  --prefix        URL prefix for the router (default: /app for plain, /htmx for htmx)
  --force         Overwrite existing generated files
  --tables-mod    Python module to introspect (default: tables)
  --out-routes    Output path for the routes file (default: app_routes.py / htmx_routes.py)
  --out-templates Output directory for templates (default: templates/app / templates/htmx)
"""

