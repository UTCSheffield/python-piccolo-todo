    lines.append("async def app_index(request: Request):")
    lines.append("    user = await get_authenticated_user(request)")
    lines.append(f'    ctx = await _app_context(user) if user else {{"user": None}}')
    lines.append(f'    return templates.TemplateResponse(request, "{tdir}/page.html", ctx)')
    lines.append("")

    # login / logout
    lines.append("")
    lines.append("# ---------------------------------------------------------------------------")
    lines.append("# Auth")
    lines.append("# ---------------------------------------------------------------------------")
    lines.append("")
    lines.append('@router.post("/login")')
    lines.append("async def app_login(request: Request, username: str = Form(...), password: str = Form(...)):")
    lines.append("    user_id = await BaseUser.login(username=username, password=password)")
    lines.append("    if not user_id:")
    lines.append("        # Re-render login page with error message")
    lines.append(f'        return templates.TemplateResponse(request, "{tdir}/page.html",')
    lines.append(f'            {{"user": None, "error": "Invalid username or password"}}, status_code=401)')
    lines.append("    session = await SessionsBase.create_session(user_id=user_id)")
    lines.append(f'    response = RedirectResponse("{prefix}/", status_code=HTTP_303_SEE_OTHER)')
    lines.append("    set_session_cookie(response=response, token=session.token)")
    lines.append("    return response")
    lines.append("")
    lines.append('@router.post("/logout")')
    lines.append("async def app_logout(request: Request):")
    lines.append('    token = request.cookies.get("id")')
    lines.append("    if token:")
    lines.append("        await SessionsBase.remove_session(token=token)")
    lines.append(f'    response = RedirectResponse("{prefix}/", status_code=HTTP_303_SEE_OTHER)')
    lines.append("    clear_session_cookie(response)")
    lines.append("    return response")
    lines.append("")

    # per-table CRUD
    owned_tables = [t for t in tables if is_user_data(t, {t.__name__: t for t in tables})]
    for t in owned_tables:
        tname = t.__name__
        tvar = tname.lower()
        tplural = plural(tvar)
        cols = user_columns(t)
        bool_cols = [c for c in cols if isinstance(c, Boolean)]
        create_cols = [c for c in cols if fk_ref(c) is None or fk_ref(c).__name__ != "BaseUser"]

        lines.append("")
        lines.append("# ---------------------------------------------------------------------------")
        lines.append(f"# {tname}")
        lines.append("# ---------------------------------------------------------------------------")
        lines.append("")

        # create
        form_params = ", ".join(
            f"{col_name(c)}: int = Form(...)" if isinstance(c, ForeignKey) else
            f"{col_name(c)}: bool = Form(False)" if isinstance(c, Boolean) else
            f"{col_name(c)}: str = Form(...)"
            for c in create_cols
        )
        new_obj_args = ", ".join(f"{col_name(c)}={col_name(c)}" for c in create_cols)
        lines.append(f'@router.post("/{tplural}")')
        lines.append(f"async def app_create_{tvar}(request: Request, {form_params}):")
        lines.append(f'    user = await get_authenticated_user(request)')
        lines.append(f'    if not user: return RedirectResponse("{prefix}/", status_code=HTTP_303_SEE_OTHER)')
        lines.append(f"    await {tname}.insert({tname}({new_obj_args}))")
        lines.append(f'    return RedirectResponse("{prefix}/", status_code=HTTP_303_SEE_OTHER)')
        lines.append("")

        # toggle — POST only (HTML forms don't support PUT/PATCH)
        for bc in bool_cols:
            bcn = col_name(bc)
            lines.append(f'@router.post("/{tplural}/{{item_id}}/toggle_{bcn}")')
            lines.append(f"async def app_toggle_{tvar}_{bcn}(request: Request, item_id: int):")
            lines.append(f'    user = await get_authenticated_user(request)')
            lines.append(f'    if not user: return RedirectResponse("{prefix}/", status_code=HTTP_303_SEE_OTHER)')
            lines.append(f"    item = await {tname}.objects().where({tname}.id == item_id).first()")
            lines.append(f"    if item:")
            lines.append(f"        await {tname}.update({{{tname}.{bcn}: not item.{bcn}}}).where({tname}.id == item_id)")
            lines.append(f'    return RedirectResponse("{prefix}/", status_code=HTTP_303_SEE_OTHER)')
            lines.append("")

        # delete — also POST (HTML forms can't DELETE)
        lines.append(f'@router.post("/{tplural}/{{item_id}}/delete")')
        lines.append(f"async def app_delete_{tvar}(request: Request, item_id: int):")
        lines.append(f'    user = await get_authenticated_user(request)')
        lines.append(f'    if not user: return RedirectResponse("{prefix}/", status_code=HTTP_303_SEE_OTHER)')
        lines.append(f"    await {tname}.delete().where({tname}.id == item_id)")
        lines.append(f'    return RedirectResponse("{prefix}/", status_code=HTTP_303_SEE_OTHER)')
        lines.append("")

    return "\n".join(lines)


