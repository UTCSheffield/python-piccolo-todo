def gen_routes(tables: list[type[Table]]) -> str:
    """Generate htmx_routes.py content."""

    # Collect all referenced foreign tables (excluding BaseUser) so we can
    # import them and pre-load their rows for select dropdowns.
    fk_refs: dict[str, type[Table]] = {}
    for t in tables:
        for col in user_columns(t):
            ref = fk_ref(col)
            if ref and ref.__name__ != "BaseUser":
                fk_refs[ref.__name__] = ref

    all_tables = {t.__name__: t for t in tables}
    all_tables.update(fk_refs)

    import_names = sorted(all_tables.keys())

    lines: list[str] = []

    lines.append('"""')
    lines.append("HTMX frontend routes — generated by scripts/scaffold_htmx.py")
    lines.append("")
    lines.append("Edit freely.  Re-run the scaffold to regenerate from scratch (use --force).")
    lines.append('"""')
    lines.append("")
    lines.append("from fastapi import APIRouter, Form, Request")
    lines.append("from fastapi.responses import HTMLResponse, Response")
    lines.append("from fastapi.templating import Jinja2Templates")
    lines.append("from piccolo.apps.user.tables import BaseUser")
    lines.append("from piccolo_api.session_auth.tables import SessionsBase")
    lines.append("")
    lines.append("from auth_helpers import clear_session_cookie, get_authenticated_user, set_session_cookie")
    lines.append(f"from tables import {', '.join(import_names)}")
    lines.append("")
    lines.append("router = APIRouter(prefix=\"/htmx\")")
    lines.append("templates = Jinja2Templates(directory=\"templates\")")
    lines.append("")

    # ---- shared helper to render the app shell ----
    lines.append("")
    lines.append("# ---------------------------------------------------------------------------")
    lines.append("# Shared helper")
    lines.append("# ---------------------------------------------------------------------------")
    lines.append("")
    lines.append("async def _app_context(user: BaseUser) -> dict:")
    lines.append('    """Fetch all data needed to render the app shell."""')
    lines.append("    ctx: dict = {\"user\": user}")

    # Collect names already added to avoid duplicates
    added: set[str] = set()

    # FK reference tables first (so they're available for dropdowns)
    for ref_name, ref_cls in fk_refs.items():
        var = plural(ref_name.lower())
        lines.append(f"    ctx[\"{var}\"] = await {ref_name}.select()")
        added.add(ref_name)

    for t in tables:
        var = plural(t.__name__.lower())
        cols = user_columns(t)
        fk_cols = [c for c in cols if fk_ref(c) and fk_ref(c).__name__ != "BaseUser"]
        user_fk_cols = [c for c in cols if fk_ref(c) and fk_ref(c).__name__ == "BaseUser"]
        all_tables_map = {t.__name__: t for t in tables}
        if user_fk_cols:
            # Directly owned — filter by user FK
            where_clause = f".where({t.__name__}.{col_name(user_fk_cols[0])} == user.id)"
        elif is_user_data(t, all_tables_map):
            # Transitively owned — student should add filtering via parent FK
            where_clause = "  # TODO: filter by owner, e.g. .where(...)"
        else:
            where_clause = ""
        if fk_cols:
            join_args = ", ".join(f"{t.__name__}.{col_name(c)}._.name" for c in fk_cols)
            lines.append(f"    ctx[\"{var}\"] = await {t.__name__}.select({t.__name__}.all_columns(), {join_args}){where_clause}")
        elif t.__name__ not in added:
            lines.append(f"    ctx[\"{var}\"] = await {t.__name__}.select(){where_clause}")

    lines.append("    return ctx")
    lines.append("")

    # ---- full page ----
    lines.append("")
    lines.append("# ---------------------------------------------------------------------------")
    lines.append("# GET /htmx  — full page")
    lines.append("# ---------------------------------------------------------------------------")
    lines.append("")
    lines.append("@router.get(\"/\", response_class=HTMLResponse)")
    lines.append("async def htmx_index(request: Request):")
    lines.append("    user = await get_authenticated_user(request)")
    lines.append("    ctx = await _app_context(user) if user else {\"user\": None}")
    lines.append("    return templates.TemplateResponse(request, \"htmx/page.html\", ctx)")
    lines.append("")

    # ---- login / logout ----
    lines.append("")
    lines.append("# ---------------------------------------------------------------------------")
    lines.append("# Auth")
    lines.append("# ---------------------------------------------------------------------------")
    lines.append("")
    lines.append("@router.post(\"/login\")")
    lines.append("async def htmx_login(request: Request, username: str = Form(...), password: str = Form(...)):")
    lines.append("    user_id = await BaseUser.login(username=username, password=password)")
    lines.append("    if not user_id:")
    lines.append("        return templates.TemplateResponse(request, \"htmx/_login.html\",")
    lines.append("            {\"error\": \"Invalid username or password\"}, status_code=401)")
    lines.append("    session = await SessionsBase.create_session(user_id=user_id)")
    lines.append("    response = Response(status_code=204, headers={\"HX-Redirect\": \"/htmx/\"})")
    lines.append("    set_session_cookie(response=response, token=session.token)")
    lines.append("    return response")
    lines.append("")
    lines.append("@router.post(\"/logout\")")
    lines.append("async def htmx_logout(request: Request):")
    lines.append("    token = request.cookies.get(\"id\")")
    lines.append("    if token:")
    lines.append("        await SessionsBase.remove_session(token=token)")
    lines.append("    response = Response(status_code=204, headers={\"HX-Redirect\": \"/htmx/\"})")
    lines.append("    clear_session_cookie(response)")
    lines.append("    return response")
    lines.append("")

    # ---- per-table CRUD (for all user-data tables: direct or transitive BaseUser ownership) ----
    all_tables_map = {t.__name__: t for t in tables}
    owned_tables = [t for t in tables if is_user_data(t, all_tables_map)]
    for t in owned_tables:
        tname = t.__name__
        tvar = tname.lower()
        tplural = plural(tvar)
        cols = user_columns(t)
        bool_cols = [c for c in cols if isinstance(c, Boolean)]
        fk_cols = [c for c in cols if fk_ref(c) and fk_ref(c).__name__ != "BaseUser"]
        # columns to include in create form (no FK to BaseUser, no bool toggle on create)
        create_cols = [c for c in cols if fk_ref(c) is None or fk_ref(c).__name__ != "BaseUser"]

        lines.append("")
        lines.append("# ---------------------------------------------------------------------------")
        lines.append(f"# {tname}")
        lines.append("# ---------------------------------------------------------------------------")
        lines.append("")

        # create
        form_params = ", ".join(
            f"{col_name(c)}: int = Form(...)" if isinstance(c, ForeignKey) else
            f"{col_name(c)}: bool = Form(False)" if isinstance(c, Boolean) else
            f"{col_name(c)}: str = Form(...)"
            for c in create_cols
        )
        new_obj_args = ", ".join(
            f"{col_name(c)}={col_name(c)}" for c in create_cols
        )
        lines.append(f"@router.post(\"/{tplural}\", response_class=HTMLResponse)")
        lines.append(f"async def htmx_create_{tvar}(request: Request, {form_params}):")
        lines.append(f"    user = await get_authenticated_user(request)")
        lines.append(f"    if not user: return HTMLResponse(\"Unauthorised\", status_code=401)")
        lines.append(f"    await {tname}.insert({tname}({new_obj_args}))")
        lines.append(f"    ctx = await _app_context(user)")
        lines.append(f"    return templates.TemplateResponse(request, \"htmx/_{tvar}_table.html\", ctx)")
        lines.append("")
        # list (GET) — called on page load by HTMX hx-trigger="load"
        lines.append(f'@router.get("/{tplural}", response_class=HTMLResponse)')
        lines.append(f"async def htmx_list_{tvar}(request: Request):")
        lines.append(f"    user = await get_authenticated_user(request)")
        lines.append(f"    if not user: return HTMLResponse(\"Unauthorised\", status_code=401)")
        lines.append(f"    ctx = await _app_context(user)")
        lines.append(f"    return templates.TemplateResponse(request, \"htmx/_{tvar}_table.html\", ctx)")
        lines.append("")
        # toggle (only if there are boolean columns)
        for bc in bool_cols:
            bcn = col_name(bc)
            lines.append(f"@router.post(\"/{tplural}/{{item_id}}/toggle_{bcn}\", response_class=HTMLResponse)")
            lines.append(f"async def htmx_toggle_{tvar}_{bcn}(request: Request, item_id: int):")
            lines.append(f"    user = await get_authenticated_user(request)")
            lines.append(f"    if not user: return HTMLResponse(\"Unauthorised\", status_code=401)")
            lines.append(f"    item = await {tname}.objects().where({tname}.id == item_id).first()")
            lines.append(f"    if item:")
            lines.append(f"        await {tname}.update({{{tname}.{bcn}: not item.{bcn}}}).where({tname}.id == item_id)")
            lines.append(f"    ctx = await _app_context(user)")
            lines.append(f"    return templates.TemplateResponse(request, \"htmx/_{tvar}_table.html\", ctx)")
            lines.append("")

        # delete
        lines.append(f"@router.delete(\"/{tplural}/{{item_id}}\", response_class=HTMLResponse)")
        lines.append(f"async def htmx_delete_{tvar}(request: Request, item_id: int):")
        lines.append(f"    user = await get_authenticated_user(request)")
        lines.append(f"    if not user: return HTMLResponse(\"Unauthorised\", status_code=401)")
        lines.append(f"    await {tname}.delete().where({tname}.id == item_id)")
        lines.append(f"    ctx = await _app_context(user)")
        lines.append(f"    return templates.TemplateResponse(request, \"htmx/_{tvar}_table.html\", ctx)")
        lines.append("")

    return "\n".join(lines)


