def main():
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--style", default="plain", choices=["plain", "htmx"],
                        help="plain = Post/Redirect/Get, no JS (default); htmx = partial swaps")
    parser.add_argument("--prefix", default=None,
                        help="URL prefix for the router (default: /app for plain, /htmx for htmx)")
    parser.add_argument("--tables-mod", default="tables", help="Python module containing Piccolo Table subclasses")
    parser.add_argument("--out-routes", default=None, help="Output path for the routes file")
    parser.add_argument("--out-templates", default=None, help="Output directory for templates")
    parser.add_argument("--force", action="store_true", help="Overwrite existing files")
    args = parser.parse_args()

    # Apply style-dependent defaults
    is_plain = args.style == "plain"
    prefix = args.prefix or ("/app" if is_plain else "/htmx")
    tdir = prefix.strip("/")
    routes_path = Path(args.out_routes or (f"{tdir}_routes.py"))
    out_dir = Path(args.out_templates or f"templates/{tdir}")

    # Add project root to sys.path so we can import the tables module.
    sys.path.insert(0, str(Path(__file__).parent.parent))

    mod = importlib.import_module(args.tables_mod)
    tables = discover_tables(mod)

    if not tables:
        print(f"No Piccolo Table subclasses found in '{args.tables_mod}'. Nothing generated.")
        sys.exit(1)

    print(f"Style:  {args.style}")
    print(f"Prefix: {prefix}")
    print(f"Found tables: {[t.__name__ for t in tables]}")

    fk_refs: dict[str, type[Table]] = {}
    for t in tables:
        for col in user_columns(t):
            ref = fk_ref(col)
            if ref and ref.__name__ != "BaseUser":
                fk_refs[ref.__name__] = ref

    all_tables_map = {t.__name__: t for t in tables}
    owned_tables = [t for t in tables if is_user_data(t, all_tables_map)]

    out_dir.mkdir(parents=True, exist_ok=True)

    def write(path: Path, content: str):
        if path.exists() and not args.force:
            print(f"  SKIP  {path}  (already exists — use --force to overwrite)")
            return
        path.write_text(content)
        print(f"  WRITE {path}")

    if is_plain:
        write(routes_path, gen_routes_plain(tables, prefix))
        write(out_dir / "page.html", gen_page_html_plain(prefix))
        write(out_dir / "_login.html", gen_login_html_plain(prefix))
        write(out_dir / "_app.html", gen_app_html_plain(tables, fk_refs, prefix))
        for t in owned_tables:
            write(out_dir / f"_{t.__name__.lower()}_table.html", gen_table_html_plain(t, prefix))
    else:
        write(routes_path, gen_routes(tables))
        write(out_dir / "page.html", gen_page_html(tables))
        write(out_dir / "_login.html", gen_login_html())
        write(out_dir / "_app.html", gen_app_html(tables, fk_refs))
        for t in owned_tables:
            write(out_dir / f"_{t.__name__.lower()}_table.html", gen_table_html(t))

    print()
    print("Done! Next steps:")
    print(f"  1. Review {routes_path} and adjust ownership/filtering as needed.")
    print(f"  2. In app.py add:")
    print(f"       from {routes_path.stem} import router as {tdir}_router")
    print(f"       app.include_router({tdir}_router)")
    print(f"     (skip if already done)")
    print(f"  3. Run: python -m uvicorn app:app --reload")
    print(f"  4. Visit: http://localhost:8000{prefix}/")


if __name__ == "__main__":
    main()
