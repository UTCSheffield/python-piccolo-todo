def discover_tables(module) -> list[type[Table]]:
    """Return all non-abstract Piccolo Table subclasses defined in module."""
    tables = []
    for _, obj in inspect.getmembers(module, inspect.isclass):
        if (
            issubclass(obj, Table)
            and obj is not Table
            and obj.__module__ == module.__name__
        ):
            tables.append(obj)
    return tables


def user_columns(table: type[Table]):
    """Return columns excluding the primary key (Serial)."""
    return [c for c in table._meta.columns if not isinstance(c, Serial)]


def col_name(col) -> str:
    return col._meta.name


def fk_ref(col) -> type[Table] | None:
    """Return the referenced Table class for a ForeignKey column, or None."""
    if not isinstance(col, ForeignKey):
        return None
    return col._foreign_key_meta.references


def label(name: str) -> str:
    """Turn snake_case into Title Case for display."""
    return name.replace("_", " ").title()


def plural(name: str) -> str:
    if name.endswith("y"):
        return name[:-1] + "ies"
    if name.endswith("s"):
        return name + "es"
    return name + "s"


def is_user_data(t: type[Table], all_tables: dict[str, type[Table]], visited: set | None = None) -> bool:
    """Return True if this table is directly or transitively owned by BaseUser.

    Examples:
      Todo(user=FK(BaseUser))         → True  (direct)
      Measurement(survey=FK(Survey))
        where Survey(user=FK(BaseUser)) → True  (transitive)
      Category(name=Varchar)          → False (pure lookup, no path to BaseUser)
    """
    if visited is None:
        visited = set()
    if t.__name__ in visited:
        return False  # avoid cycles
    visited.add(t.__name__)
    for col in user_columns(t):
        ref = fk_ref(col)
        if ref is None:
            continue
        if ref.__name__ == "BaseUser":
            return True
        # Check if the referenced table is itself user-owned
        if ref.__name__ in all_tables and is_user_data(ref, all_tables, visited):
            return True
    return False


