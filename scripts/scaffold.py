def gen_app_html(tables: list[type[Table]], fk_refs: dict) -> str:
    """Generate _app.html with a create form and table section per table."""
    sections: list[str] = []

    sections.append("<section id=\"app\">")
    sections.append("  <p>")
    sections.append("    Logged in as <strong>{{ user.username }}</strong>")
    sections.append("    <button hx-post=\"/htmx/logout\" hx-confirm=\"Log out?\" style=\"margin-left:1rem\">Log out</button>")
    sections.append("  </p>")
    sections.append("")

    # Only render CRUD sections for user-data tables (direct or transitive BaseUser ownership).
    # Lookup tables like Category are managed via /admin — no frontend CRUD needed.
    all_tables_map = {t.__name__: t for t in tables}
    owned_tables = [t for t in tables if is_user_data(t, all_tables_map)]
    for t in owned_tables:
        tname = t.__name__
        tvar = tname.lower()
        tplural = plural(tvar)
        cols = user_columns(t)
        create_cols = [c for c in cols if fk_ref(c) is None or fk_ref(c).__name__ != "BaseUser"]

        sections.append(f"  <!-- ---- {tname} ---- -->")
        sections.append(f"  <h2>{label(tplural)}</h2>")
        sections.append("")
        sections.append(f"  <form hx-post=\"/htmx/{tplural}\" hx-target=\"#{tvar}-table\" hx-swap=\"outerHTML\"")
        sections.append(f"        hx-on::after-request=\"this.reset()\" style=\"display:flex;gap:0.5rem;flex-wrap:wrap;margin-bottom:1rem\">")

        for c in create_cols:
            cname = col_name(c)
            ref = fk_ref(c)
            if ref:
                ref_var = plural(ref.__name__.lower())
                sections.append(f"    <select name=\"{cname}\" required>")
                sections.append(f"      {{% for item in {ref_var} %}}")
                sections.append(f"        <option value=\"{{{{ item.id }}}}\">{{{{ item.name }}}}</option>")
                sections.append(f"      {{% endfor %}}")
                sections.append(f"    </select>")
            elif isinstance(c, Boolean):
                sections.append(f"    <label><input name=\"{cname}\" type=\"checkbox\" /> {label(cname)}</label>")
            else:
                sections.append(f"    <input name=\"{cname}\" type=\"text\" placeholder=\"{label(cname)}\" required />")

        sections.append(f"    <button type=\"submit\">Add {label(tvar)}</button>")
        sections.append(f"  </form>")
        sections.append("")
        sections.append(f"  <div id=\"{tvar}-table\" hx-get=\"/htmx/{tplural}\"")
        sections.append(f"       hx-trigger=\"load\" hx-swap=\"outerHTML\">")
        sections.append(f"    <p>Loading {tplural}…</p>")
        sections.append(f"  </div>")
        sections.append("")

    sections.append("</section>")
    return "\n".join(sections)


def gen_table_html(t: type[Table]) -> str:
    tname = t.__name__
    tvar = tname.lower()
    tplural = plural(tvar)
    cols = user_columns(t)
    bool_cols = [c for c in cols if isinstance(c, Boolean)]
    fk_cols = [c for c in cols if isinstance(c, ForeignKey) and fk_ref(c) and fk_ref(c).__name__ != "BaseUser"]
    display_cols = [c for c in cols if not isinstance(c, Boolean) and fk_ref(c) is None]

    lines = [f'<table id="{tvar}-table">']
    lines.append("  <thead><tr>")
    for c in display_cols:
        lines.append(f"    <th>{label(col_name(c))}</th>")
    for c in fk_cols:
        lines.append(f"    <th>{label(col_name(c))}</th>")
    for c in bool_cols:
        lines.append(f"    <th>{label(col_name(c))}</th>")
    lines.append("    <th></th>")
    lines.append("  </tr></thead>")
    lines.append(f"  <tbody>")
    lines.append(f"  {{% for item in {tvar}s %}}")
    lines.append("  <tr>")
    for c in display_cols:
        cname = col_name(c)
        lines.append(f"    <td>{{{{ item.{cname} }}}}</td>")
    for c in fk_cols:
        cname = col_name(c)
        lines.append(f"    <td>{{{{ item['{cname}.name'] or '—' }}}}</td>")
    for c in bool_cols:
        cname = col_name(c)
        lines.append(f"    <td>")
        lines.append(f"      <button hx-post=\"/htmx/{tplural}/{{{{ item.id }}}}/toggle_{cname}\"")
        lines.append(f"              hx-target=\"#{tvar}-table\" hx-swap=\"outerHTML\">")
        lines.append(f"        {{{{ '✅' if item.{cname} else '⬜' }}}}")
        lines.append(f"      </button>")
        lines.append(f"    </td>")
    lines.append(f"    <td>")
    lines.append(f"      <button hx-delete=\"/htmx/{tplural}/{{{{ item.id }}}}\"")
    lines.append(f"              hx-target=\"#{tvar}-table\" hx-swap=\"outerHTML\"")
    lines.append(f"              hx-confirm=\"Delete this {label(tvar)}?\">🗑</button>")
    lines.append(f"    </td>")
    lines.append("  </tr>")
    lines.append("  {% else %}")
    lines.append(f"  <tr><td colspan=\"10\" style=\"text-align:center;color:#888\">No {tplural} yet.</td></tr>")
    lines.append("  {% endfor %}")
    lines.append("  </tbody>")
    lines.append("</table>")
    return "\n".join(lines)


